package com.example.a444app;

public class UserInformation {
    public String uname;
    public String uemail;
    public String uid;
    public String upassword;
    public String uphone;


    public UserInformation() {
    }

    public UserInformation(String uname, String uemail, String uid, String upassword, String uphone) {
        this.uname = uname;
        this.uemail = uemail;
        this.uid = uid;
        this.upassword = upassword;
        this.uphone = uphone;
    }
}
